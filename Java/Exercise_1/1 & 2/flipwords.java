public class flipwords{
	public static void main (String[]args){
		System.out.println(flipthis("gray"));
	}

	public static char[] flipthis(String words_to_be_flipped){
		char[] splitted_words = words_to_be_flipped.toCharArray();

		for(int start = 0, end = words_to_be_flipped.length()-1 ; start < end ; start++, end--){
			char temp;
			temp = splitted_words[start];
			splitted_words[start] = splitted_words[end];
			splitted_words[end]=temp;
		}

		return splitted_words;
	}
}































































































































































































































































































































































